package com.codesquad.dust3.airquality;

public class Coordinates {
    private double x;
    private double y;

    public Coordinates(double x, double yCoordinate) {
        this.x = x;
        this.y = yCoordinate;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
